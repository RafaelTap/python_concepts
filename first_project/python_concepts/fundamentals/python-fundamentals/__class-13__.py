grade1 = float(input("Enter the first grade: ")) # input is like the class Scanner in java
grade2 = float(input("Enter the second grade: "))
grade3 = float(input("Enter the third grade: "))

average = (grade1 + grade2 + grade3) / 3

print(f"the grade average is: {average:.2f}") #to show only 2 or more decimal places